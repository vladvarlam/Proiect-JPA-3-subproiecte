    private void adaugaClientProdus(){
       // CodFidelizare cf=new CodFidelizare();
        
        Client c=new Client();
        
    String nume=jTextField1.getText();
    String cod=jTextField2.getText();
    String judet=jTextField3.getText();
    String tel1=jTextField4.getText();
    String tel2=jTextField5.getText();
    c.setJudet(judet);
    c.setNume(nume);
    c.setCod(cod);
    c.setTelefoane(new ArrayList<>());
    c.getTelefoane().add(tel1);
    c.getTelefoane().add(tel2);
    
    
        Eticheta et1=new Eticheta();
        et1.setDepartament("ALIMENTAR");
        
        Eticheta et2=new Eticheta();
        et2.setDepartament("ELECTRICE");
        
        
        c.setProduse(new ArrayList<>());
        Produs p1 = new Produs();
        p1.setNume(jTextField6.getText());
        p1.setPret(Double.parseDouble(jTextField7.getText()));
        
        p1.setClient(c);
        p1.setEticheta(et1);
        c.getProduse().add(p1);
        
        Produs p2=new Produs();
        p2.setNume(jTextField8.getText());
        p2.setPret(Double.parseDouble(jTextField9.getText()));
        
        p2.setClient(c);
        p2.setEticheta(et2);
        c.getProduse().add(p2);
        
        ClientController.getInstance().adaugaClientPro(c);
        JOptionPane.showMessageDialog(null, "Am adaugat clientul si produsele sale");
        //dispose();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        jTextField9.setText("");
        display();
    }
    
